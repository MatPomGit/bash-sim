#!/usr/bin/env bash
# lib/colors.sh – ANSI color and style definitions

# Reset
RESET="\033[0m"

# Regular colors
BLACK="\033[0;30m"
RED="\033[0;31m"
GREEN="\033[0;32m"
YELLOW="\033[0;33m"
BLUE="\033[0;34m"
MAGENTA="\033[0;35m"
CYAN="\033[0;36m"
WHITE="\033[0;37m"

# Bold colors
BOLD="\033[1m"
BOLD_RED="\033[1;31m"
BOLD_GREEN="\033[1;32m"
BOLD_YELLOW="\033[1;33m"
BOLD_BLUE="\033[1;34m"
BOLD_MAGENTA="\033[1;35m"
BOLD_CYAN="\033[1;36m"
BOLD_WHITE="\033[1;37m"

# Dark/dim
DIM="\033[2m"
ITALIC="\033[3m"
UNDERLINE="\033[4m"

# Background colors
BG_BLACK="\033[40m"
BG_RED="\033[41m"
BG_GREEN="\033[42m"
BG_YELLOW="\033[43m"
BG_BLUE="\033[44m"
BG_MAGENTA="\033[45m"
BG_CYAN="\033[46m"
BG_WHITE="\033[47m"

# Semantic aliases
COLOR_TITLE="${BOLD_CYAN}"
COLOR_STORY="${CYAN}"
COLOR_SUCCESS="${BOLD_GREEN}"
COLOR_ERROR="${BOLD_RED}"
COLOR_WARNING="${BOLD_YELLOW}"
COLOR_PLAYER="${BOLD_GREEN}"
COLOR_ENEMY="${BOLD_RED}"
COLOR_ITEM="${BOLD_YELLOW}"
COLOR_COMMAND="${BOLD_WHITE}"
COLOR_HINT="${DIM}${CYAN}"
COLOR_XP="${BOLD_MAGENTA}"
COLOR_GOLD="${BOLD_YELLOW}"
COLOR_HP_HIGH="${GREEN}"
COLOR_HP_MED="${YELLOW}"
COLOR_HP_LOW="${RED}"
COLOR_PROMPT="${BOLD_WHITE}"
COLOR_BORDER="${BLUE}"
